
  // Education.ts
  class Education {
    institution: string;
    degree: string;
    fieldOfStudy: string;
    graduationYear: number;
  
    constructor(institution: string, degree: string, fieldOfStudy: string, graduationYear: number) {
      this.institution = institution;
      this.degree = degree;
      this.fieldOfStudy = fieldOfStudy;
      this.graduationYear = graduationYear;
    }
  }